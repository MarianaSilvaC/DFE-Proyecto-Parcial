document.addEventListener('DOMContentLoaded', () => {
    // Selecciona la imagen del "Proyecto 1"
    const proyecto1Imagen = document.querySelector('#imagen-pro1');

    // Selecciona la ventana emergente del "Proyecto 1"
    const proyecto1Ventana = document.querySelector('#proyecto1');

    // Agrega un EventListener para el clic en la imagen
    proyecto1Imagen.addEventListener('click', () => {
        // Muestra la ventana emergente del "Proyecto 1"
        proyecto1Ventana.style.display = 'block';
    });

    // Agrega un EventListener para el clic en la ventana emergente para cerrarla
    proyecto1Ventana.addEventListener('click', () => {
        // Oculta la ventana emergente del "Proyecto 1"
        proyecto1Ventana.style.display = 'none';
    });

});

